<h2 id="html" tabindex="-1"><a class="header-anchor" href="#html" aria-hidden="true">#</a> HTML</h2>
<p>在前面已经说过了，因为之前做项目是一种一边做一边搜索的过程，没有去做一个完整的学习，所以在这次WEB之前的我对HTML是很散很乱的。</p>
<p>HTML是一个web页面的钢筋混凝土的墙，是主要搭载我们的页面内容的墙。HTML作为一种超文本标记语言，在网页上使用一些简单的标签，如 <code>body、head、title、p、img、a、div、span、ol、table</code> 等，即可搭建起页面的骨架，简单但却有效。当HTML配合起 <code>CSS、JavaScript</code>后就可做非常多的变化，但是这是有条件的，在之前不了解HTML一些标签的特性的时候，想完成一些功能时会让人难以下手又或者写的代码非常复杂冗余，不够优雅。想要好好的做一个网页还是需要足够了解HTML的，想要绕过这一过程，直接使用某某组件库、某某框架同样做不出一个好的页面。</p>
<h2 id="css" tabindex="-1"><a class="header-anchor" href="#css" aria-hidden="true">#</a> CSS</h2>
<p>在上一段中，说到了和HTML和CSS的配合，CSS的全称是<code>Cascading Style Sheets 层叠样式表</code> 是为结构化文档添加样式的语言。
对于一个页面来说，有效、优雅的CSS可以页面变得赏心悦目。像一些背景、边距、阴影、边框等等是CSS一些比较基本的操作。但是对于如何处理背景上的文字不清、一些鼠标触发的动效、如何让整体风格统一是需要比较深入CSS才能做到的</p>
<p>CSS最强大的地方可能是它的布局了，页面可以通过<code>display</code>中的<code>flex</code>、<code>Grid</code>来调整整个网页的布局排版，再通过媒体查询<code>@media</code>又可以让<code>html</code>的内容在各种设备上优雅的展示页面的内容。在真正的学习CSS之前，我只会一会基本的布局，而这一点基本的布局根本无法满足现代化页面的需求，更无法满足页面变化时页面不会变畸形的要求。又回到上面说过的，要有好的CSS基础才能让开发变得更加灵活，这是高度封装的组件库很难做到的。</p>
