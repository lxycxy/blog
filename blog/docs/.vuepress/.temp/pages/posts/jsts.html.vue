<h2 id="javascript" tabindex="-1"><a class="header-anchor" href="#javascript" aria-hidden="true">#</a> JavaScript</h2>
<p>毋庸置疑JavaScript是一门强大且灵活的编程语言，对JS最初的认识就是一简单的脚本语言，但随着对JS的学习，发现了JS似乎有着非常大的体量。初始认识就是一些基于对象、弱类型、跨平台等，随着深入慢慢的就会了解一些闭包、异步、内存管理等等，现在的JS已经不是简单的脚本语言了，而是拥有前端、后端开发能力的编程语言。</p>
<p>在刚刚开始使用JS进行web开发时，常常写错一些不知觉的错误代码，而浏览器报出的错误会让人难以理解又或者不会报错，在某段代码中使用了<code>for</code>循环遍历对象数组，但却使用的是<code>in</code>。浏览器并没有报错，但是页面部分内容却消失了，而这个错误往往会花费大量的时间来检查。</p>
<p>从另一些角度来说，JS还存在一定的问题——弱类型。弱类型、解释型会让JS在一些不规范的代码下出现错误，而这样的错误出现后，可能会导致后续的代码执行走向未知的错误。可以想到如果给JS加上类型检查或限制，是不是就可以解决这类问题？于是有了这样一门语言——TypeScript</p>
<h2 id="typescript" tabindex="-1"><a class="header-anchor" href="#typescript" aria-hidden="true">#</a> TypeScript</h2>
<p>如前面所说，TS就是JS再加上类型之后的一门语言，在编译后TS可转化为JS，除了类型问题，TS加入更多的、更规范的代码要求。
例如，使用<code>let、const</code>并且可以加入类型声明、函数参数、返回值有完整的类型等。这些特性会让TS代码变得规范，不容易出现一些低级的类型错误等，会很大程度提高检查错误的效率；另外就是在一定程度上提高了开发人员的代码风格，会让代码更加易读。</p>
<p>目前的TypeScript在web开发中用的火热，社区生态非常的活跃，许多框架和组件库都开始兼容TS，甚至有些会更加推荐你使用TS，TS在未来会发展的更加好。</p>
